 # Python program to demonstrate    
 # Addition of elements in a List   
      
# Creating a List   
List = [] 
      
# Using append() 
List.append(1)   
List.append(2) 
print(List)   
    
# Using insert() 
List.insert(3, 12)   
List.insert(0, 'Monika') 
print(List)   
    
# Using extend()   
List.extend([8, 'Monika', 'Sharma'])   
print(List)
